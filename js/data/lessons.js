/**
 * Получить урок по ID
 */
export function getLessonById(lessonId) {
    return lessons.find(lesson => lesson.id === lessonId) || null;
}

/**
 * Получить общее количество уроков
 */
export function getTotalLessons() {
    return lessons.length;
}

/**
 * Получить следующий урок (для разблокировки)
 */
export function getNextLessonId(currentLessonId) {
    const index = lessons.findIndex(l => l.id === currentLessonId);
    if (index !== -1 && index + 1 < lessons.length) {
        return lessons[index + 1].id;
    }
    return null;
}